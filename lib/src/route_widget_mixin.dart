// Marker mixin that the RouteBuilder's TRouteWidgetBuilder enforces as the
// return type. This guarantees every route widget carries its RouteState,
// enabling the router to pass route metadata (query params, extras) to the
// widget without a separate lookup mechanism.
mixin RouteWidgetMixin<TExtra extends Object?> on Widget {
  RouteState<TExtra?>? get routeState;
}
